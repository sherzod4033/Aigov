const LocaleContext = createContext(null);

const interpolate = (template, variables) => String(template).replace(/\{(\w+)\}/g, (_, key) => String(variables?.[key] ?? ''));

const getTranslationValue = (locale, key) => {
    const segments = String(key || '').split('.');
    let current = translations[locale];

    for (const segment of segments) {
        if (current == null || typeof current !== 'object') return undefined;
        current = current[segment];
    }

    return current;
};

export const normalizeLocale = (value) => {
    const normalized = String(value || '').trim().toLowerCase();

    if (normalized === 'tj') return 'tg';
    if (normalized === 'tg' || normalized === 'ru') return normalized;
    return null;
};

export const resolveInitialLocale = () => {
    if (typeof window === 'undefined') return DEFAULT_LOCALE;

    const storedLocale = normalizeLocale(localStorage.getItem(LOCALE_STORAGE_KEY));
    if (storedLocale) return storedLocale;

    const legacyLocale = normalizeLocale(localStorage.getItem(LEGACY_LANG_STORAGE_KEY));
    if (legacyLocale) return legacyLocale;

    return DEFAULT_LOCALE;
};

export const persistLocale = (locale) => {
    if (typeof window === 'undefined') return;

    const normalized = normalizeLocale(locale) || DEFAULT_LOCALE;
    localStorage.setItem(LOCALE_STORAGE_KEY, normalized);
    localStorage.setItem(LEGACY_LANG_STORAGE_KEY, normalized === 'tg' ? 'TJ' : 'RU');
};

export const LocaleProvider = ({ children }) => {
    const [locale, setLocaleState] = useState(resolveInitialLocale);

    const setLocale = useCallback((nextLocale) => {
        const normalized = normalizeLocale(nextLocale) || DEFAULT_LOCALE;
        setLocaleState(normalized);
        persistLocale(normalized);
    }, []);

    useEffect(() => {
        document.documentElement.lang = locale;
        document.title = getTranslationValue(locale, 'meta.appTitle') || 'SafeDocsAI';
    }, [locale]);

    const t = useCallback((key, variables) => {
        const translated = getTranslationValue(locale, key) ?? getTranslationValue('ru', key) ?? key;
        return typeof translated === 'string' ? interpolate(translated, variables) : translated;
    }, [locale]);

    const value = useMemo(() => ({ locale, setLocale, t }), [locale, setLocale, t]);

    return <LocaleContext.Provider value={value}>{children}</LocaleContext.Provider>;
};

export const useLocale = () => {
    const context = useContext(LocaleContext);
    if (!context) {
        throw new Error('useLocale must be used within LocaleProvider');
    }
    return context;
};
